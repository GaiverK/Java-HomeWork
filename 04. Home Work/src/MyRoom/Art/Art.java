package MyRoom.Art;

public class Art {
    String name;
    int width;
    int height;
    boolean onTheWall;
    String description;

    public Art(String name, int width, int height, boolean onTheWall, String description) {
        this.name = name;
        this.width = width;
        this.height = height;
        this.onTheWall = onTheWall;
        this.description = description;
    }

}
